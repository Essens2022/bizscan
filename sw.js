const CACHE_NAME = 'bizscan-shell-v1';

self.addEventListener('install', (event) => {
  self.skipWaiting();
});

self.addEventListener('activate', (event) => {
  event.waitUntil(
    caches.keys().then((keys) =>
      Promise.all(keys.filter((k) => k !== CACHE_NAME).map((k) => caches.delete(k)))
    )
  );
  self.clients.claim();
});

// Strategia "rete prima" - controlla sempre la versione più recente quando c'è connessione;
// usa la cache solo come rete di sicurezza se offline. Questo garantisce che l'app
// installata rifletta sempre le modifiche più recenti al sito, senza bisogno di
// alcuna azione da parte dell'utente.
self.addEventListener('fetch', (event) => {
  if (event.request.method !== 'GET') return;
  event.respondWith(
    fetch(event.request)
      .then((response) => {
        const copy = response.clone();
        caches.open(CACHE_NAME).then((cache) => cache.put(event.request, copy));
        return response;
      })
      .catch(() => caches.match(event.request))
  );
});
